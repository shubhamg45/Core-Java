class ForExample1
{
	public static void main(String[] args) {
		
		// A TO  Z

		for (char ch = 'A';ch<='Z' ;ch++ ) {

			System.out.print(ch+ " ");
			
		}
		System.out.println();
		System.out.println("-----------------------------------------");
        

          // a to Z

		for (char ch = 'a';ch<='z' ;ch++ ) {

			System.out.print(ch+ " ");
			
		}
		System.out.println();
		System.out.println("-----------------------------------------");

		// 0 to 9


		for (char ch = 48;ch<=57;ch++ ) {

			System.out.print(ch+ " ");
			
		}
	}
}